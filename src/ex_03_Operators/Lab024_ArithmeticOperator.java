package ex_03_Operators;

public class Lab024_ArithmeticOperator {
    public static void main(String[] args) {
        // Arithmatic Operator
        // + Addition
        // - Substraction
        // * Multiplication
        // / Division
        // % Modulus -> remainder

        int a = 10;
        int b = 5;
        float c = 2.5f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a/c);

    }
}
