package ex_08_DoWhileLoop;

public class Lab057_DoWhile {
    public static void main(String[] args) {

        //here condition will be checked once you enter the loop
        //So for one time you will enter the loop
        int a = 0 ;
        do{
            System.out.println(a);
            a++;
        }while(a<10);

    }
}
